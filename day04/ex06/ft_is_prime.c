int	ft_is_prime(int nb)
{
	int i;

	if ((nb % 2 == 0 && nb != 2) || nb < 2)
		return (0);
	i = 3;
	while (i * i <= nb)
	{
		if (nb % i == 0)
			return (0);
		i += 2;
		if (nb == 46341)
			return (1);
	}
	return (1);
}
