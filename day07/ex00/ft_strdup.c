#include <stdlib.h>

char	*ft_strdup(char *src)
{
	char	*dest;
	int		length;
	int		i;

	length = 0;
	while (src[length])
		length++;
	dest = (char*)malloc(sizeof(*dest) * length + 1);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
